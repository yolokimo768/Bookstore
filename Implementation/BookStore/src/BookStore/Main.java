    private void BookuserTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BookuserTableMouseClicked
        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        int column = source.columnAtPoint(evt.getPoint());
        //String s = source.getModel().getValueAt(row, column) + "";
        String ISBN = source.getModel().getValueAt(row, 0) + "";
        String Title = source.getModel().getValueAt(row, 1) + "";
        String Genre = source.getModel().getValueAt(row, 2) + "";
        String Price = source.getModel().getValueAt(row, 3) + "";
        String Publisher = source.getModel().getValueAt(row, 4) + "";
        DataBaseOperations DB = null;
        try {

            String Select_Book_Query = "SELECT firstname , lastname from Author INNER JOIN Author_Book ON Author.A_id=Author_Book.A_id where isbn ='" + ISBN + "';";
            DB = new DataBaseOperations();
            String authors = "";
            ResultSet res = DB.Select(Select_Book_Query);
            while (res.next()) {
                authors += res.getString(1) + "," + res.getString(2) + " - ";
            }
//ISBN , Title, Genre, Price, Name
            ISBNLabelvalue.setText(ISBN);
            genreLabelvalue.setText(Genre);
            publisherLabelvalue.setText(Publisher);
            priceLabelvalue.setText(Price);
            authorsLabelvalue1.setText(authors);
            titleLabelvalue.setText(Title);

            Panelbookuserdata.setVisible(true);
            bookselected.setISBN(ISBN);
            bookselected.setTitle(Title);
            bookselected.setPrice(Double.parseDouble(Price));
            DB.Closed();
        } catch (Exception e) {
            e.printStackTrace();
            DB.Closed();

        }

    }//GEN-LAST:event_BookuserTableMouseClicked

    private void BuyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuyBtnActionPerformed
        // TODO add your handling code here:
        if (quantitybookaddText.getText().equals("") || shipaddressText.getText().equals("") || billaddressText.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "You Must wrtie the order detials(Quantity, Ship and Bill address)");
        } else {
            int sel = JOptionPane.showConfirmDialog(null, "Are You sure to buy this book");
            if (sel == 0) {

                DataBaseOperations DB = null;
                try {
                    DB = new DataBaseOperations();
                    double totalcost = bookselected.getPrice() * (Integer.parseInt(quantitybookaddText.getText()));
                    long millis = System.currentTimeMillis();
                    Date date = new java.sql.Date(millis);
                    String Insert_Orders_Query = "INSERT INTO Orders (date, totalcost, shippingaddress, billingaddress, location, shippingservice, u_id) VALUES "
                            + "('" + date + "'," + totalcost + ",'" + shipaddressText.getText() + "','" + billaddressText.getText() + "','Arrived','DHL',1);";

                    DB.Insert(Insert_Orders_Query);
                    DB = new DataBaseOperations();
                    String Select_Orders_Query = "Select o_id from Orders where date = '" + date + "';";

                    ResultSet res = DB.Select(Select_Orders_Query);
                    res.next();
                    int Order_ID = res.getInt(1);
                    DB = new DataBaseOperations();
                    String Insert_Order_User_Query = "INSERT INTO Order_Book (ISBN, O_id, Quantity) VALUES "
                            + "('" + bookselected.getISBN() + "'," + Order_ID + "," + (Integer.parseInt(quantitybookaddText.getText())) + ");";

                    DB.Insert(Insert_Order_User_Query);

                    DB.Closed();
                    JOptionPane.showMessageDialog(null, "Book is Ordered Successfully");
                } catch (Exception e) {
                    e.printStackTrace();
                    DB.Closed();

                }
            } else {
                quantitybookaddText.setText("");
                shipaddressText.setText("");
                billaddressText.setText("");
                Panelbookuserdata.setVisible(false);

            }
        }

    }//GEN-LAST:event_BuyBtnActionPerformed

